#!/usr/bin/env python3
"""Module for Basic annotations - concat function"""


def concat(str1: str, str2: str) -> str:
    """concat: type-annotated function that takes two strings and return a
       concatenated string
    Args:
       str1: sring to perform concatenation
       str2: sring to perform concatenation
    Return:
       the concatenated string of str1 and str2
    """
    return str1 + str2
