#!/usr/bin/env python3
"""Module for Basic annotations - add function"""


def add(a: float, b: float) -> float:
    """add: type-annotated function that takes two floats and return their
       sum as a float
    Args:
       a: float number to perform add operation
       b: float number to perform add operation
    Return:
       the result of adding a and b
    """
    return a + b
